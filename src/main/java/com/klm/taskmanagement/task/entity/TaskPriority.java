package com.klm.taskmanagement.task.entity;
/**
 * Enum representing the priority level of a task.
 * Can be used to help sort or filter tasks based on urgency.
 */
public enum TaskPriority {
    /** Lowest priority - not urgent. */
    LOW,
    /** Medium priority - standard importance. */
    MEDIUM,
    /** Highest priority - needs immediate attention. */
    HIGH
}
